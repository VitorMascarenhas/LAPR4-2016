    private void lstCommentsResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstCommentsResultMouseClicked
        if(evt.getClickCount() == 2) {
            int index = lstCommentsResult.locationToIndex(evt.getPoint());
            Comment cc = (Comment)lstCommentsResult.getModel().getElementAt(index);
            EditCommentsController ecc = new EditCommentsController(uiController);
            jDialogEdit = new JDialog();
            EditCommentPanel ecp = new EditCommentPanel(uiController, ecc, jDialogEdit, cc);
            jDialogEdit.add(ecp);
            jDialogEdit.setVisible(true);
            jDialogEdit.pack();
        }
        if(evt.getClickCount() == 1) {
            int index = lstCommentsResult.locationToIndex(evt.getPoint());
            CommentableCell cell = (CommentableCell)uiController.getActiveCell().getExtension(CommentsExtension.NAME);
            Comment cc = (Comment)lstCommentsResult.getModel().getElementAt(index);
            lblIdValue.setText(String.valueOf(cc.returnCommentId()));
            lblCount.setText(String.valueOf(cell.returnNumberOfId(cc.returnCommentId())));
            
        }
    }//GEN-LAST:event_lstCommentsResultMouseClicked


