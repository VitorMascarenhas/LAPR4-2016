    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        String str = txtCommentSearch.getText();
        DefaultListModel dlm = new DefaultListModel();
        DefaultListModel temp = this.scc.searchForExpression(str);
        
        int size = temp.size();
        
        for(int i = 0;i < size; i++) {
            if(temp.get(i).toString().contains(str)) {
                Comment c = (Comment)temp.get(i);
                dlm.addElement(c);
            }
        }
        this.lstCommentsResult.setModel(dlm);
    }//GEN-LAST:event_btnSearchActionPerformed

