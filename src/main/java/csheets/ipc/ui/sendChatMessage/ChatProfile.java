    private void btOkActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btOkActionPerformed
    {//GEN-HEADEREND:event_btOkActionPerformed
        if (!jtNick.getText().isEmpty() && (btOffline.isSelected() || btOnline.isSelected())) {
            String nick = jtNick.getText();
            boolean option;
            option = btOnline.isSelected();
            UserChatProfile user = new UserChatProfile(null, nick, option, WIDTH, this.icon);
            sendChatMessagePanel.setUserProfile(user);
            JOptionPane.showMessageDialog(null, "User profile created successfully!");
            sendChatMessagePanel.enableConnect();
            this.jDialog.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "User information is not complete!");
        }

    }//GEN-LAST:event_btOkActionPerformed

