    /**
     * added by 1140411 Returns an ImageIcon, or null if the path was invalid.
     *
     * @param path
     * @param description
     * @return
     */
    protected ImageIcon createImageIcon(String path, String description)
    {
        Image image = new ImageIcon(path, description).getImage().getScaledInstance(20, 20, Image.SCALE_DEFAULT);
        return new ImageIcon(image);
    }

